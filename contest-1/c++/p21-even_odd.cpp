// Write a program to Check Whether a Number is Even or Odd.

#include <iostream>
#include <algorithm>
using namespace std;


int main() {
    /* Enter your code here. Read input from STDIN. Print output to STDOUT */
    int n;
    cin>>n;
    if(n%2==0) cout<<"EVEN";
    else cout<<"ODD";
    return 0;
}
